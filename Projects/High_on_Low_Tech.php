After our collective’s recent dispersal across Europe, we stay in touch using infrastructures owned by genocidal tech-giants. Finding ourselves in an age of platform capitalism and technofascism has led us to experiment with alternative structures, with a focus on the way our communication shapes the way we understand each other’s localities. 
<br><br>
Our project High on Low Tech aims to build up a collection of images to act as the base from which a collective meme culture can grow and develop. Meme culture is a phenomenon that started developing on the internet in the early 2000s. A meme is an easily digestible piece of media, often referencing popular culture, often, but not always visual. It is a result of a culture that’s has been disconnected from each other directly and developed a way to still create things together. It shapes generations as it’s utilized to bridge the distance placed between us by our economic system. Referencing Hito Steyerl's essay In Defense of the Poor Image, we argue that meme culture at its core is a never ending process of found photography. We ultimately aim to create conditions for the images to be collected, discovered and reused. 
<br><br>
The images that form the motheryeast of the collective meme culture come from our previous works, our recent meetups and our documentation of each others works. Here we open our process to everyone, if you want stickers, let us know, we can print some out and send them to you. You can download the print files and print them out yourself. Please send us images of how the stickers live their lives, how they change and evolve in your localities to become memes in the next locality!
